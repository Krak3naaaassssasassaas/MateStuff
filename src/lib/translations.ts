export type TranslationKey = keyof typeof translations;

type TranslationFunction = (key: TranslationKey, params?: { [key: string]: string | number }) => string;

const createTranslateFunction = (language: 'en' | 'id'): TranslationFunction => {
  return (key, params) => {
    let translation = translations[key]?.[language] || translations[key]?.['en'] || key;
    if (params) {
      Object.keys(params).forEach(paramKey => {
        translation = translation.replace(`{${paramKey}}`, String(params[paramKey]));
      });
    }
    return translation;
  };
};

export { createTranslateFunction };
